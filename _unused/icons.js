// Icon getter function with size parameter
function getIcon(name, size = 20) {
    const iconSvg = icons[name];
    if (!iconSvg) {
        console.warn(`Icon '${name}' not found`);
        return '';
    }
    
    // Update size in the SVG
    return iconSvg.replace(/width="\d+"/, `width="${size}"`).replace(/height="\d+"/, `height="${size}"`);
}

